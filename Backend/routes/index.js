//////////////////////////////////////////////////////////////////////////////////////////
import { Router }  from 'express'
//////////////////////////////////////////////////////////////////////////////////////////
const router = Router()
//////////////////////////////////////////////////////////////////////////////////////////
router.get('/',(req, res)=>{

  const info = {
    "name":process.env.npm_package_name || "",
    "version":process.env.npm_package_version || ""
  }

  res.send(info)
})
//////////////////////////////////////////////////////////////////////////////////////////
export default router
